# Built-in Variable

# Built-in variables are variables that are defined in the built-in scope
# Built-in variables are accessible inside the function
# Built-in variables are created when the program is started and destroyed when the program is exited

# examples: Python built-in

# boolean
print(type(True))

# None
print(type(None))

# numbers
print(type(1))
print(type(1.0))
print(type(1+2j))

# sequence
print(type([1, 2, 3]))
print(type((1, 2, 3)))
print(type("hello"))

# mapping
print(type({"a": 1, "b": 2}))

# set
print(type({1, 2, 3}))

# function
print(type(lambda x: x))

# class
print(type(object))

# module
print(type(__import__("math")))

# file
print(type(open("test.txt", "w")))

# exception
print(type(Exception()))

# iterator
print(type(iter([1, 2, 3])))

# generator
print(type((x for x in range(3))))

# bytes
print(type(b"hello"))

# bytearray
print(type(bytearray(b"hello")))

# memoryview
print(type(memoryview(b"hello")))

